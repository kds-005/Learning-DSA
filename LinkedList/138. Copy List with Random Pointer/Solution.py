from typing import Optional

# Definition for a Node.
class Node:
    def __init__(self, x: int, next: 'Node' = None, random: 'Node' = None):
        self.val = int(x)
        self.next = next
        self.random = random


class Solution:
    def copyRandomList(self, head: 'Optional[Node]') -> 'Optional[Node]':
        # N : no of nodes
        # Time Complexity: O(N)
        # Space Complexity: O(N)
        nodeRandMap = {None: None}

        curr = head
        while curr:
            nodeRandMap[curr] = Node(curr.val)
            curr = curr.next
        
        curr = head
        while curr:
            nodeRandMap[curr].next = nodeRandMap[curr.next]
            nodeRandMap[curr].random = nodeRandMap[curr.random]
            curr = curr.next
        return nodeRandMap[head]